import java.util.Date;

public class SongMusic {
	int id_song;
	String name_song;
	String author_song;
	Date date_add_song;
	Boolean public_song;
	
	public int getId_song() {
		return id_song;
	}
	public void setId_song(int id_song) {
		this.id_song = id_song;
	}
	public String getName_song() {
		return name_song;
	}
	public void setName_song(String name_song) {
		this.name_song = name_song;
	}
	public String getAuthor_song() {
		return author_song;
	}
	public void setAuthor_song(String author_song) {
		this.author_song = author_song;
	}
	public Date getDate_add_song() {
		return date_add_song;
	}
	public void setDate_add_song(Date date_add_song) {
		this.date_add_song = date_add_song;
	}
	public Boolean getPublic_song() {
		return public_song;
	}
	public void setPublic_song(Boolean public_song) {
		this.public_song = public_song;
	}
	
	
	

}
